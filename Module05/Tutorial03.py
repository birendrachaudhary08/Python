
#string Method or modify Statement

a=" Python is a popular programming language. "

#The upper() method returns the string in upper case:
upper_case = a.upper()
print(upper_case)
print("\n")


#The lower() method returns the string in lower case:
lowe_case = a.lower()
print(lowe_case)
print("\n")

#The strip() method removes any whitespace from the beginning or the end:
remove_whitespace=a.strip()
print(remove_whitespace)
print("\n")

#The replace() method replaces a string with another string:
replace_string = a.replace("Python", "JAVA")
print(replace_string)
print("\n")

#split() method returns a list where the text between the specified separator becomes the list items.
split_sting = a.split()
print(split_sting)
print("\n")